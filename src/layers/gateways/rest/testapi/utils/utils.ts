export function ToSqlDateTime(time: Date): string {
  return time.toISOString().slice(0, 19).replace('T', ' ');
}

export function DateToSeconds(time: Date): number {
  return Math.round(Number(time) / 1000);
}

export function DateFromSeconds(time: number): Date {
  return new Date(time * 1000);
}

